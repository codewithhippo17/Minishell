#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			l_s;
	char			*ns;

	i = 0;
	l_s = ft_strlen(s);
	if (!s)
		return (NULL);
	ns = (char *)malloc((l_s + 1) * sizeof(char));
	if (!ns)
		return (NULL);
	while (s[i])
	{
		ns[i] = f(i, s[i]);
		i++;
	}
	ns[i] = '\0';
	return (ns);
}
